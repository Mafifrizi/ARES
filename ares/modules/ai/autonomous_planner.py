async def _run_adversarial_simulation(
    attack_plan: dict,
    backend: "LLMBackend",
) -> dict:
    """
    Second LLM call: simulate defender response to the generated attack plan.
    Returns detection timeline, blind spots, and attacker recommendations.
    """
    plan_summary = json.dumps({
        "stages": [
            {"name": s.get("name"), "modules": s.get("modules", [])}
            for s in attack_plan.get("stages", [])
        ]
    }, indent=2)

    user_prompt = (
        f"Analyze this red team attack sequence and predict defender detection:\n\n"
        f"{plan_summary}\n\n"
        f"For each stage, identify: which SIEM rules fire, how quickly, "
        f"and what the attacker could change to avoid detection."
    )

    try:
        result = await backend.generate_plan(ADVERSARIAL_SYSTEM_PROMPT, user_prompt)
        raw = result.get("content", "{}")
        raw = raw.strip()
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]
        if raw.endswith("```"):
            raw = raw.rsplit("```", 1)[0]
        return json.loads(raw.strip())
    except Exception as exc:
        return {
            "error": str(exc)[:150],
            "detections": [],
            "blind_spots": [],
            "overall_detection_probability": None,
        }



