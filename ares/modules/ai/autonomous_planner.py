class AIAutonomousPlannerModule(BaseModule):
    """
    ai.autonomous_planner — LLM-powered attack chain orchestration.

    Aggregates campaign context and uses an LLM to generate the optimal
    next attack steps. Outputs a structured ExecutionPlan for operator review.

    OPSEC: LOCAL (LLM API call — no direct target network contact from this module)
    MITRE: T1591
    REQUIRES: active_campaign
    OUTPUTS:  execution_plan, ai_reasoning, confidence_score, warnings
    """
    MODULE_ID          = "ai.autonomous_planner"
    MODULE_NAME        = "AI Autonomous Attack Planner"
    MODULE_CATEGORY    = "ai"
    MODULE_DESCRIPTION = (
        "Use LLM (Claude/GPT/Ollama) to analyze campaign context and generate "
        "an optimal attack chain. Outputs ExecutionPlan for operator review."
    )
    MODULE_AUTHOR      = "ARES Team <team@ares-framework.io>"
    OPSEC_LEVEL        = OpsecLevel.LOCAL
    REQUIRES           = []
    OUTPUTS            = ["execution_plan", "ai_reasoning", "confidence_score", "warnings"]
    MITRE_TECHNIQUES   = ["T1591"]
    MODULE_TIMEOUT_SECONDS: int | None = 180

    async def validate(self, ctx: "Any") -> None:
        """Pre-flight: need active campaign and LLM API key."""
        await super().validate(ctx)
        from ares.core.context import ExecutionContext
        from ares.core.errors import ModuleValidationError
        if not isinstance(ctx, ExecutionContext):
            return
        import os
        backend = ctx.params.get("llm_backend", "claude")
        if backend == "claude" and not os.environ.get("ANTHROPIC_API_KEY"):
            raise ModuleValidationError(
                "ai.autonomous_planner with backend=claude requires ANTHROPIC_API_KEY env var. "
                "Set it or use backend=openai (OPENAI_API_KEY) or backend=local (Ollama).",
                module_id=self.MODULE_ID, field="llm_backend",
            )
        if backend == "openai" and not os.environ.get("OPENAI_API_KEY"):
            raise ModuleValidationError(
                "ai.autonomous_planner with backend=openai requires OPENAI_API_KEY env var.",
                module_id=self.MODULE_ID, field="llm_backend",
            )

    async def execute(self, ctx: "Any") -> ModuleResult:
        """ExecutionContext-based entry point (v0.9.0+).
        Thin adapter: extract params from ctx → call run() → return ModuleResult.
        """
        if getattr(ctx, "dry_run", False):
            return ModuleResult(
                status="dry_run", module_id=self.MODULE_ID,
                raw={"dry_run": True, "note": "Would generate LLM attack plan from campaign context"},
            )
        findings, raw = await self.run(
            campaign=ctx.campaign,
            vault=getattr(ctx, "vault", None),
            goal=ctx.params.get("goal", "domain_admin"),
            llm_backend=ctx.params.get("llm_backend", "claude"),
            llm_model=ctx.params.get("llm_model", ""),
            auto_approve=ctx.params.get("auto_approve", False),
        )
        return ModuleResult(
            status="success" if findings else "partial",
            findings=findings, raw=raw, module_id=self.MODULE_ID,
            execution_id=getattr(ctx, "execution_id", ""),
        )

    @trace_module("ai.autonomous_planner")
    async def run(
        self,
        campaign:     Any  = None,
        vault:        Any  = None,
        goal:         str  = "domain_admin",
        llm_backend:  str  = "claude",
        llm_model:    str  = "",
        auto_approve:   bool = False,
        extra_context:  dict | None = None,
        **kwargs: Any,
    ) -> tuple[list, dict]:
        """
        Note: before_request() intentionally not called — LOCAL module.
        LLM API call has no scope relevance (calls Anthropic/OpenAI, not target).
        """
        audit("ai_planner_invoked", actor="operator",
              technique="T1591", source="operator",
              target="llm_api", detail=f"backend={llm_backend} goal={goal}")

        logger.info("ai_planner_start", backend=llm_backend, goal=goal)

        # Build context
        builder = CampaignContextBuilder()
        context = builder.build(campaign, vault, goal)
        # BUG 8 FIX: inject StrategyEngine extra_context before building prompt
        if extra_context:
            context.update(extra_context)

        # Select backend
        backend_map = {
            "claude": lambda: ClaudeBackend(llm_model or "claude-opus-4-6"),
            "openai": lambda: OpenAIBackend(llm_model or "gpt-4o"),
            "local":  lambda: LocalOllamaBackend(llm_model or "llama3.2"),
        }
        backend_factory = backend_map.get(llm_backend, backend_map["claude"])
        backend = backend_factory()

        user_prompt = _build_user_prompt(context)

        # Get system prompt with engagement-specific constitution
        scope_cidrs = [str(s.cidr) for s in getattr(campaign, "scope", [])]
        system_prompt = _build_system_prompt_with_constitution(
            scope_cidrs=scope_cidrs,
            engagement_type=kwargs.get("engagement_type", "full_compromise"),
            authorizations=kwargs.get("authorizations"),
        )

        # Multi-LLM consensus when secondary backend provided
        secondary_backend_name = kwargs.get("secondary_backend", "")
        use_consensus = bool(secondary_backend_name)

        try:
            if use_consensus and secondary_backend_name != llm_backend:
                # Run both LLMs in parallel
                backend2_map = {
                    "claude": lambda: ClaudeBackend(kwargs.get("secondary_model", "")),
                    "openai": lambda: OpenAIBackend(kwargs.get("secondary_model", "")),
                    "local":  lambda: LocalOllamaBackend(kwargs.get("secondary_model", "")),
                }
                backend2 = backend2_map.get(secondary_backend_name, backend_map["claude"])()
                result_a, result_b = await asyncio.gather(
                    backend.generate_plan(system_prompt, user_prompt),
                    backend2.generate_plan(system_prompt, user_prompt),
                    return_exceptions=True,
                )
                if isinstance(result_a, Exception) or isinstance(result_b, Exception):
                    raise (result_a if isinstance(result_a, Exception) else result_b)
                plan_a = json.loads(result_a["content"].strip().lstrip("```json").rstrip("```"))
                plan_b = json.loads(result_b["content"].strip().lstrip("```json").rstrip("```"))
                merged = _merge_plans(plan_a, plan_b)
                raw_content = json.dumps(merged)
                model_used  = f"{result_a['model']}+{result_b['model']}"
                tokens_used = result_a.get("tokens_used", 0) + result_b.get("tokens_used", 0)
            else:
                llm_result  = await backend.generate_plan(system_prompt, user_prompt)
                raw_content = llm_result["content"]
                model_used  = llm_result["model"]
                tokens_used = llm_result["tokens_used"]
        except Exception as exc:
            logger.error("ai_planner_llm_failed", error=str(exc)[:200])
            return [], {
                "error": f"LLM API call failed: {str(exc)[:200]}",
                "execution_plan":   None,
                "ai_reasoning":     "",
                "confidence_score": 0.0,
                "warnings":         [],
            }

        # Parse LLM response
        ai_plan = self._parse_llm_response(raw_content, model_used, tokens_used)

        # Validate plan before generating ExecutionPlan
        try:
            from ares.modules.ai.plan_validator import PlanValidator
            validator = PlanValidator()
            val_errors = validator.validate(
                ai_plan,
                registry=getattr(kwargs.get("ares_engine"), "registry", None),
                campaign=campaign,
            )
            if val_errors:
                logger.warning("ai_plan_validation_failed", errors=val_errors)
                return [], {
                    "error":           val_errors,
                    "execution_plan":  None,
                    "ai_reasoning":    ai_plan.reasoning[:200],
                    "confidence_score": 0.0,
                    "warnings":        ai_plan.warnings,
                }
        except Exception as val_exc:
            logger.warning("plan_validator_error", error=str(val_exc)[:80])

        # Generate ExecutionPlan
        exec_plan_dict = [
            {"name": s["name"], "modules": s.get("modules", []), "params": s.get("params", {})}
            for s in ai_plan.stages
        ]

        logger.info("ai_plan_generated",
                    stages=len(ai_plan.stages),
                    confidence=ai_plan.confidence,
                    tokens=ai_plan.tokens_used,
                    model=ai_plan.llm_model)

        # Generate findings
        self._generate_findings(ai_plan, goal, context)

        # Adversarial simulation — what would defender see?
        adversarial_result = {}
        if kwargs.get("adversarial_sim", False) and ai_plan.stages:
            try:
                adversarial_result = await _run_adversarial_simulation(
                    {"stages": ai_plan.stages}, backend
                )
                logger.info("adversarial_sim_complete",
                            detection_prob=adversarial_result.get("overall_detection_probability"))
            except Exception as adv_exc:
                adversarial_result = {"error": str(adv_exc)[:100]}

        raw = {
            "execution_plan":      exec_plan_dict,
            "ai_reasoning":        ai_plan.reasoning,
            "confidence_score":    ai_plan.confidence,
            "warnings":            ai_plan.warnings,
            "alternative_plan":    ai_plan.alternative,
            "llm_model":           ai_plan.llm_model,
            "tokens_used":         ai_plan.tokens_used,
            "context_hosts":       len(context["hosts"]),
            "context_creds":       len(context["credentials"]),
            "auto_approve":        auto_approve,
            "plan_ready":          not auto_approve,
            "consensus_used":      use_consensus,
            "adversarial_sim":     adversarial_result,
        }
        return self._findings[:], raw

    def _parse_llm_response(self, content: str, model: str, tokens: int) -> AIPlan:
        """Parse LLM JSON response into AIPlan."""
        try:
            # Strip markdown fences if present
            content = content.strip()
            if content.startswith("```"):
                content = content.split("```")[1]
                if content.startswith("json"):
                    content = content[4:]
            if content.endswith("```"):
                content = content.rsplit("```", 1)[0]

            data = json.loads(content.strip())
            return AIPlan(
                reasoning=    data.get("reasoning", ""),
                stages=       data.get("stages", []),
                confidence=   float(data.get("confidence", 0.5)),
                alternative=  data.get("alternative", []),
                warnings=     data.get("warnings", []),
                llm_model=    model,
                tokens_used=  tokens,
            )
        except Exception as exc:
            logger.warning("ai_plan_parse_failed", error=str(exc)[:100], raw=content[:200])
            # Return fallback plan
            return AIPlan(
                reasoning=  f"LLM response parse failed: {str(exc)[:100]}. Raw: {content[:200]}",
                stages=     [],
                confidence= 0.0,
                warnings=   ["LLM response was not valid JSON — manual planning required"],
                llm_model=  model,
                tokens_used=tokens,
            )

    def _generate_findings(self, plan: AIPlan, goal: str, context: dict) -> None:
        if not plan.stages:
            self.finding(
                title="AI Planner: No Attack Stages Generated",
                description=(
                    f"LLM could not generate a viable attack plan for goal '{goal}'. "
                    f"Confidence: {plan.confidence:.0%}. "
                    f"Reasoning: {plan.reasoning[:300]}"
                ),
                severity=Severity.INFO,
                mitre_technique="T1591",
                mitre_tactic="Reconnaissance",
                evidence={"goal": goal, "warnings": plan.warnings},
                remediation="Add more recon data — run ad.enum_users, recon.fingerprint first.",
                host="ai_planner",
                confidence=0.50,
            )
            return

        # Summary finding
        stage_names = [s.get("name", "?") for s in plan.stages]
        all_modules  = [m for s in plan.stages for m in s.get("modules", [])]

        self.finding(
            title=f"AI Attack Plan Generated — {len(plan.stages)} Stages, "
                  f"{len(all_modules)} Modules, {plan.confidence:.0%} Confidence",
            description=(
                f"Goal: {goal}. "
                f"AI Reasoning: {plan.reasoning[:400]}. "
                f"Plan: {' → '.join(stage_names)}. "
                f"Modules: {', '.join(all_modules[:10])}{'...' if len(all_modules) > 10 else ''}. "
                f"Model: {plan.llm_model} ({plan.tokens_used:,} tokens)."
            ),
            severity=Severity.INFO,
            mitre_technique="T1591",
            mitre_tactic="Planning",
            evidence={
                "stages":         stage_names,
                "modules":        all_modules,
                "confidence":     plan.confidence,
                "warnings":       plan.warnings,
                "tokens_used":    plan.tokens_used,
                "llm_model":      plan.llm_model,
            },
            remediation="Review plan before execution. Use engine.run_plan() with operator approval.",
            host="ai_planner",
            confidence=plan.confidence,
        )

        # Warn about high-confidence dangerous steps
        for warning in plan.warnings:
            self.finding(
                title=f"AI Planner OPSEC Warning: {warning[:80]}",
                description=warning,
                severity=Severity.MEDIUM,
                mitre_technique="T1591",
                mitre_tactic="Planning",
                evidence={"warning": warning},
                remediation="Review this warning before executing the plan.",
                host="ai_planner",
                confidence=0.80,
            )
