def _score_plan(plan: dict) -> float:
    """Score a raw plan dict for consensus merging — higher = better."""
    stages     = plan.get("stages", [])
    confidence = float(plan.get("confidence", 0.5))
    modules    = [m for s in stages for m in s.get("modules", [])]
    warnings   = len(plan.get("warnings", []))
    return confidence * (1 + len(modules) * 0.05) - warnings * 0.1


def _merge_plans(plan_a: dict, plan_b: dict) -> dict:
    """
    Merge two LLM plan responses into a consensus plan.

    Agreement algorithm:
    - Modules appearing in BOTH plans → high confidence, keep
    - Modules in only ONE plan → lower confidence, keep with warning
    - confidence = average of both * agreement_bonus
    - warnings merged and deduplicated
    """
    stages_a = {s["name"]: s for s in plan_a.get("stages", [])}
    stages_b = {s["name"]: s for s in plan_b.get("stages", [])}

    all_names = list(dict.fromkeys(list(stages_a) + list(stages_b)))
    merged_stages = []
    agreement_count = 0

    for name in all_names:
        sa = stages_a.get(name)
        sb = stages_b.get(name)

        if sa and sb:
            # Both LLMs suggested this stage — high agreement
            agreement_count += 1
            mods_a = set(sa.get("modules", []))
            mods_b = set(sb.get("modules", []))
            agreed_mods   = list(mods_a & mods_b)
            disputed_mods = list((mods_a | mods_b) - (mods_a & mods_b))

            # Merge params — prefer plan_a for conflicts
            merged_params = {**sb.get("params", {}), **sa.get("params", {})}

            merged_stages.append({
                "name":          name,
                "rationale":     sa.get("rationale", ""),
                "modules":       agreed_mods + disputed_mods,
                "params":        merged_params,
                "consensus":     True,
                "agreed_modules":  agreed_mods,
                "disputed_modules": disputed_mods,
            })
        elif sa:
            merged_stages.append({**sa, "consensus": False, "source": "llm_a"})
        else:
            merged_stages.append({**sb, "consensus": False, "source": "llm_b"})

    total_stages = max(len(stages_a), len(stages_b))
    agreement_ratio = 1.0 if total_stages == 0 else agreement_count / total_stages
    conf_a = float(plan_a.get("confidence", 0.5))
    conf_b = float(plan_b.get("confidence", 0.5))
    merged_conf = (conf_a + conf_b) / 2
    if total_stages:
        merged_conf *= 0.8 + 0.4 * agreement_ratio

    warnings = list(dict.fromkeys(
        plan_a.get("warnings", []) + plan_b.get("warnings", [])
    ))
    if agreement_ratio < 1.0:
        warnings.append(
            f"LOW CONSENSUS: LLMs agreed on only {agreement_ratio:.0%} of stages — "
            "operator review strongly recommended before execution."
        )

    return {
        "reasoning":      f"[Consensus] A: {plan_a.get('reasoning','')[:150]} | "
                          f"B: {plan_b.get('reasoning','')[:150]}",
        "confidence":     min(merged_conf, 1.0),
        "stages":         merged_stages,
        "alternative":    plan_a.get("alternative", []) or plan_b.get("alternative", []),
        "warnings":       warnings,
        "consensus_meta": {
            "agreement_ratio":  round(agreement_ratio, 3),
            "agreed_stages":    agreement_count,
            "total_stages":     total_stages,
            "llm_a_confidence": conf_a,
            "llm_b_confidence": conf_b,
        },
    }


