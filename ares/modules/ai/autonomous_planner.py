def _build_round_narrative(completed_rounds: list) -> str:
    """Build LLM-readable narrative of completed rounds for memory injection."""
    if not completed_rounds:
        return "Round 1 — no prior history."

    lines = ["=== HISTORY ROUNDS SEBELUMNYA ==="]
    for r in completed_rounds[-3:]:   # max 3 rounds back
        lines.append(
            f"Round {r.round_num} "
            f"(confidence: {r.plan_confidence:.0%}, "
            f"detection: {r.detection_score:.0%}):"
        )
        for o in r.outcomes:
            icon    = "SUCCESS" if o.success else "FAILED"
            bypass  = f" [bypass: {o.bypass_used}]" if o.bypass_used else ""
            quality = f" quality={o.quality:.1f}" if hasattr(o, "quality") else ""
            lines.append(f"  [{icon}] {o.module_id}{bypass}{quality}")
            evidence = getattr(o, "evidence", "")
            if evidence:
                lines.append(f"          => {evidence}")
    return "\n".join(lines)

class CampaignContextBuilder:
    """Aggregates all campaign context into a structured dict for LLM prompt."""


    def _format_credentials_actionable(self, creds: list) -> list:
        """Format credential list with recommended modules for each credential."""
        result = []
        for cred in creds[:10]:
            priv      = getattr(cred, "privilege", "unknown") or "unknown"
            cred_type = str(getattr(cred, "cred_type", "") or "")
            cracked   = bool(getattr(cred, "plaintext", ""))

            can_use: list[str] = []
            if cred_type in ("NTLM", "plaintext"):
                can_use += ["lateral.psexec", "lateral.wmiexec", "lateral.winrm"]
            if priv in ("domain_admin", "enterprise_admin"):
                can_use += ["ad.dcsync", "credential.golden_ticket"]
            if cred_type == "kerberos_ticket":
                can_use += ["lateral.psexec", "ad.dcsync"]
            if not cracked and cred_type == "NTLM":
                can_use.insert(0, "credential.crack")

            result.append({
                "username":        getattr(cred, "username", ""),
                "domain":          getattr(cred, "domain", ""),
                "type":            cred_type,
                "privilege":       priv,
                "cracked":         cracked,
                "recommended_use": can_use[:4],
                "note": ("Cracked — ready to use" if cracked
                         else "Hash available — crack first before use"),
            })
        return result

    def build(self, campaign: Any, vault: Any = None, goal: str = "domain_admin") -> dict:
        """Build a rich context dict from all available campaign data."""
        ctx: dict[str, Any] = {
            "goal":            goal,
            "campaign_id":     getattr(campaign, "id", "unknown")[:8],
            "scope":           [str(s.cidr) for s in getattr(campaign, "scope", [])],
            "noise_profile":   str(getattr(campaign, "noise_profile", "normal")),
            "hosts":           [],
            "users":           [],
            "credentials":     [],
            "findings_summary": [],
            "failed_modules":  [],
            "available_modules": [],
        }

        # Extract hosts from findings
        seen_hosts: set[str] = set()
        findings = getattr(campaign, "findings", [])
        for f in findings[:50]:
            host = getattr(f, "host", "") or (f.get("host", "") if isinstance(f, dict) else "")
            if host and host not in seen_hosts:
                seen_hosts.add(host)
                ctx["hosts"].append({"ip": host, "services": []})

            # Summarize findings
            title = getattr(f, "title", "") or (f.get("title", "") if isinstance(f, dict) else "")
            sev   = getattr(f, "severity", "") or (f.get("severity", "") if isinstance(f, dict) else "")
            mid   = getattr(f, "module_id", "") or (f.get("module_id", "") if isinstance(f, dict) else "")
            if title:
                ctx["findings_summary"].append({"severity": str(sev), "title": title, "module": mid})

        # Extract credentials from vault
        if vault:
            try:
                creds = vault.list() if hasattr(vault, "list") else []
                ctx["credentials"] = self._format_credentials_actionable(creds[:20])
            except Exception:
                pass

        # Available attack modules
        from ares.technique.library import _MODULE_TECHNIQUE_MAP
        ctx["available_modules"] = list(_MODULE_TECHNIQUE_MAP.keys())[:40]

        # EDR bypass availability (from artifact store if fingerprint ran)
        ctx["edr_bypass_available"] = []
        try:
            store = getattr(campaign, "_artifact_store", None)
            if store:
                for art in getattr(store, "_artifacts", []):
                    if hasattr(art, "edr_vendors") and art.edr_vendors:
                        ctx["edr_bypass_available"] = [
                            v.value for v in art.edr_vendors[:3]
                        ]
        except Exception:
            pass

        # Historical success rates from KnowledgeBase
        ctx["historical_success_rates"] = {}
        try:
            from ares.knowledge.base import AttackKnowledgeBase
            kb = AttackKnowledgeBase()
            for mid in ctx["available_modules"][:15]:
                rate = getattr(kb, "get_success_rate", lambda m: None)(mid)
                if rate is not None:
                    ctx["historical_success_rates"][mid] = rate
        except Exception:
            pass

        return ctx


