@dataclass
class AIPlan:
    """Structured attack plan generated by LLM."""
    reasoning:       str                  # LLM's explanation of why this plan
    stages:          list[dict]           # [{name, modules, params, rationale}]
    confidence:      float                # 0-1 how confident LLM is in this plan
    alternative:     list[dict] = field(default_factory=list)  # backup plan
    warnings:        list[str]  = field(default_factory=list)  # OPSEC warnings
    llm_model:       str        = ""
    tokens_used:     int        = 0
    generated_at:    float      = field(default_factory=time.monotonic)

    def to_execution_plan(self) -> "ExecutionPlan":
        """Convert to ARES ExecutionPlan for engine.run_plan()."""
        from ares.core.engine import ExecutionPlan
        plan = ExecutionPlan()
        for stage in self.stages:
            plan.add_stage(
                name=stage.get("name", "ai_stage"),
                module_ids=stage.get("modules", []),
                params=stage.get("params", {}),
            )
        return plan


