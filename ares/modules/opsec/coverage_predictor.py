def _compute_soc_activity_factor(
    target_domain: str = "",
    target_type: str = "default",
    now_utc: datetime.datetime | None = None,
) -> dict:
    """
    Compute current SOC analyst activity factor (0.0-1.0) based on
    target organization's likely timezone and shift hours.

    Returns dict with factor, optimal_window, and recommendation.
    """
    if now_utc is None:
        now_utc = datetime.datetime.utcnow()

    # Determine SOC profile
    profile = _SOC_COVERAGE.get(target_type, _SOC_COVERAGE["default"])
    if target_domain:
        for tld, prof in _SOC_COVERAGE.items():
            if target_domain.endswith(tld):
                profile = prof
                break

    shift_start, shift_end = profile["shift"]
    weekend_factor = profile["weekend_factor"]
    is_weekend = now_utc.weekday() >= 5  # 5=Sat, 6=Sun

    # Estimate current local hour (approximate without pytz)
    tz_offsets = {
        "US/Eastern": -5, "US/Pacific": -8, "US/Central": -6,
        "Europe/London": 0, "Europe/Berlin": 1, "Europe/Paris": 1,
        "Australia/Sydney": 10, "Pacific/Auckland": 12,
        "Asia/Tokyo": 9, "Asia/Singapore": 8, "UTC": 0,
    }
    offset = tz_offsets.get(profile["tz"], 0)
    local_hour = (now_utc.hour + offset) % 24

    # Activity factor
    if is_weekend:
        base_factor = weekend_factor
    elif shift_start <= local_hour < shift_end:
        # Peak hours: higher in middle of shift
        hours_into_shift = local_hour - shift_start
        shift_len = shift_end - shift_start
        # Bell curve approximation
        normalized = hours_into_shift / shift_len
        base_factor = 1.0 - 4 * (normalized - 0.5) ** 2  # peaks at 50%
        base_factor = max(base_factor, 0.5)  # minimum 50% during shift
    else:
        base_factor = 0.15  # off-hours

    # Optimal attack window: 02:00-05:00 local time (dead zone)
    optimal_local_hours = []
    for h in range(24):
        target_local = (now_utc.hour + offset + h) % 24
        in_shift = shift_start <= target_local < shift_end
        factor = weekend_factor if is_weekend else (0.9 if in_shift else 0.15)
        if factor < 0.2:
            optimal_local_hours.append(target_local)

    # Find best window
    best_window = f"{optimal_local_hours[0]:02d}:00-{optimal_local_hours[-1] if optimal_local_hours else optimal_local_hours[0]:02d}:59"         if optimal_local_hours else "No optimal window found (24/7 SOC)"

    recommendation = ""
    if base_factor > 0.7:
        recommendation = (
            f"HIGH SOC ACTIVITY ({base_factor:.0%}) — defer high-noise actions. "
            f"Optimal window: {best_window} local time."
        )
    elif base_factor > 0.4:
        recommendation = (
            f"MODERATE SOC ACTIVITY ({base_factor:.0%}) — use stealth techniques only. "
            f"Better window in {max(0, shift_end - local_hour)}h."
        )
    else:
        recommendation = (
            f"LOW SOC ACTIVITY ({base_factor:.0%}) — execute now. "
            f"Optimal window: {best_window} local time."
        )

    return {
        "soc_activity_factor": round(base_factor, 3),
        "local_hour_estimate": local_hour,
        "is_weekend":          is_weekend,
        "soc_profile":         profile["tz"],
        "optimal_window":      best_window,
        "recommendation":      recommendation,
    }


