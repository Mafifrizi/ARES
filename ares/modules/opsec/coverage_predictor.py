class CoveragePredictorModule(BaseModule):
    """
    opsec.coverage_predictor — Real-time detection probability scoring

    OPSEC: LOCAL (offline analysis — no network calls)
    MITRE: T1592 (informational — analyzes existing campaign data)
    REQUIRES: active_campaign
    OUTPUTS:  detection_score, action_risks, wait_recommendation
    """
    MODULE_ID          = "opsec.coverage_predictor"
    MODULE_NAME        = "OPSEC Coverage Predictor"
    MODULE_CATEGORY    = "opsec"
    MODULE_DESCRIPTION = "Predict detection probability from campaign actions. Outputs score, per-action Event IDs, and wait-time recommendations."
    MODULE_AUTHOR      = "ARES Team <team@ares-framework.io>"
    OPSEC_LEVEL        = OpsecLevel.LOCAL
    REQUIRES           = []
    OUTPUTS            = ["detection_score", "action_risks", "wait_recommendation", "recommendations"]
    MITRE_TECHNIQUES   = ["T1592"]
    MODULE_TIMEOUT_SECONDS: int | None = 30

    async def validate(self, ctx: "Any") -> None:
        """Pre-flight: campaign must be active."""
        await super().validate(ctx)
        campaign = getattr(ctx, "campaign", None)
        if not campaign:
            from ares.core.errors import ModuleValidationError
            raise ModuleValidationError(
                "opsec.coverage_predictor requires an active campaign.",
                module_id=self.MODULE_ID, field="campaign",
            )

    async def execute(self, ctx: "Any") -> ModuleResult:
        """ExecutionContext-based entry point (v0.9.0+).
        Thin adapter: extract params from ctx → call run() → return ModuleResult.
        """
        if getattr(ctx, "dry_run", False):
            return ModuleResult(
                status="dry_run", module_id=self.MODULE_ID,
                raw={"dry_run": True, "note": "Would analyze campaign execution history"},
            )
        noise_profile = ctx.params.get("noise_profile", "normal")
        findings, raw = await self.run(
            campaign=ctx.campaign,
            noise_profile=noise_profile,
        )
        return ModuleResult(
            status="success" if findings else "partial",
            findings=findings, raw=raw, module_id=self.MODULE_ID,
            execution_id=getattr(ctx, "execution_id", ""),
        )

    @trace_module("opsec.coverage_predictor")
    async def run(
        self,
        campaign: "Any" = None,
        noise_profile: str = "normal",
        **kwargs: Any,
    ) -> tuple[list, dict]:
        # Note: before_request() intentionally not called — LOCAL module, no network.

        loop = asyncio.get_running_loop()
        prediction = await loop.run_in_executor(
            None, lambda: self._analyze(campaign, noise_profile)
        )

        audit("opsec_coverage_prediction", actor="operator",
              technique="T1592", source="operator",
              target="campaign", detail=f"score={prediction.overall_score:.2f}")

        logger.info("coverage_prediction_complete",
                    score=f"{prediction.overall_score:.1%}",
                    wait_hours=prediction.wait_hours,
                    modules_analyzed=prediction.modules_analyzed)

        # Generate findings based on risk level
        findings = []
        score_pct = int(prediction.overall_score * 100)

        if prediction.overall_score >= 0.9:
            sev = Severity.CRITICAL
        elif prediction.overall_score >= 0.6:
            sev = Severity.HIGH
        elif prediction.overall_score >= 0.4:
            sev = Severity.MEDIUM
        elif prediction.overall_score >= 0.2:
            sev = Severity.LOW
        else:
            sev = Severity.INFO

        if prediction.overall_score > 0.05:
            self.finding(
                title=f"Detection Probability: {score_pct}% — {prediction.wait_reason}",
                description=(
                    f"Based on {prediction.modules_analyzed} modules executed and "
                    f"{len(prediction.techniques_triggered)} MITRE techniques triggered, "
                    f"estimated detection probability is {score_pct}%. "
                    f"Highest-risk action: {prediction.highest_risk_action.description if prediction.highest_risk_action else 'N/A'}. "
                    f"Recommended wait before next action: {prediction.wait_hours} hours."
                ),
                severity=sev,
                mitre_technique="T1592",
                mitre_tactic="Reconnaissance",
                evidence={
                    "detection_score":      round(prediction.overall_score, 4),
                    "wait_hours":           prediction.wait_hours,
                    "techniques_triggered": prediction.techniques_triggered,
                    "top_risk_events": [
                        {"event": f"{r.event_source}:{r.event_id}",
                         "probability": round(r.probability, 3),
                         "description": r.description}
                        for r in prediction.action_risks[:5]
                    ],
                },
                remediation="\n".join(prediction.recommendations),
                host="campaign",
                confidence=0.75,
            )

        raw = {
            "detection_score":    round(prediction.overall_score, 4),
            "score_percent":      score_pct,
            "wait_recommendation": {
                "hours":  prediction.wait_hours,
                "reason": prediction.wait_reason,
            },
            "action_risks": [
                {
                    "module_id":    r.module_id,
                    "technique":    r.technique,
                    "event":        f"{r.event_source} Event {r.event_id}",
                    "probability":  round(r.probability, 3),
                    "description":  r.description,
                }
                for r in prediction.action_risks[:20]
            ],
            "recommendations":       prediction.recommendations,
            "modules_analyzed":      prediction.modules_analyzed,
            "techniques_triggered":  prediction.techniques_triggered,
            "soc_activity":          prediction.soc_activity,
            "siem_correlations":     prediction.siem_correlations,
        }

        return self._findings[:], raw


    def predict_planned_actions(
        self,
        planned_modules:  list[str],
        current_score:    float,
        campaign:         Any = None,
    ) -> dict:
        """
        Pre-execution: predict how much detection score will rise if plan executes.
        Operator can reject plan BEFORE it runs.
        Used by StrategyEngine between plan generation and execution.
        """
        from ares.technique.library import _MODULE_TECHNIQUE_MAP

        # HIGH_NOISE modules — verified against OPSEC_LEVEL in module definitions
        # lateral.wmiexec removed (MEDIUM, not HIGH_NOISE)
        # ad.coerce + windows.lsa_secrets added (confirmed HIGH_NOISE)
        _KNOWN_HIGH_NOISE = {
            "ad.dcsync",
            "ad.coerce",
            "lateral.psexec",
            "lateral.rdp",
            "windows.lsass_dump",
            "windows.lsa_secrets",
        }

        additional  = 0.0
        warnings:   list[str] = []
        breakdowns: list[dict] = []

        for mid in planned_modules:
            module_addition = 0.0
            techniques = _MODULE_TECHNIQUE_MAP.get(mid, [])

            for technique in techniques:
                events = _TECHNIQUE_DETECTION_MAP.get(technique, [])
                for _, _, prob, _desc in events:
                    module_addition += prob * 0.08  # weighted contribution

            if mid in _KNOWN_HIGH_NOISE:
                module_addition += 0.20
                warnings.append(f"{mid} is HIGH_NOISE (+20% detection contribution)")

            additional += module_addition
            breakdowns.append({"module": mid, "contribution": round(module_addition, 3)})

        projected = min(current_score + additional, 1.0)
        safe = projected < 0.60

        return {
            "current_score":    round(current_score, 3),
            "additional":       round(additional, 3),
            "projected_score":  round(projected, 3),
            "safe_to_execute":  safe,
            "warnings":         warnings,
            "module_breakdown": sorted(breakdowns, key=lambda x: -x["contribution"]),
            "recommendation": (
                "SAFE — proceed" if safe else
                f"DANGEROUS — this plan will raise detection to {projected:.0%}. "
                "Revise to stealthier techniques."
            ),
        }

    def _analyze(self, campaign: "Any", noise_profile: str) -> CoveragePrediction:
        """Build modules_run list from campaign data and compute prediction."""
        predictor = CoveragePredictor()

        # Extract module execution history from campaign findings
        modules_run = []
        findings_dicts = []

        if campaign:
            # Build modules_run from campaign findings metadata
            seen_modules: set[str] = set()
            for finding in getattr(campaign, "findings", []):
                f_dict = finding if isinstance(finding, dict) else {}
                if hasattr(finding, "__dict__"):
                    f_dict = finding.__dict__
                findings_dicts.append(f_dict)

                # Extract module info from finding metadata
                mid = f_dict.get("module_id") or f_dict.get("source_module", "")
                if mid and mid not in seen_modules:
                    seen_modules.add(mid)
                    # Look up MITRE techniques from technique library
                    from ares.technique.library import _MODULE_TECHNIQUE_MAP
                    techniques = _MODULE_TECHNIQUE_MAP.get(mid, [])
                    # Get OPSEC level from module class if available
                    modules_run.append({
                        "module_id":        mid,
                        "mitre_techniques": techniques,
                        "opsec_level":      "medium",   # default — refined below
                        "timestamp":        time.monotonic(),
                    })

        return predictor.predict(
            campaign_findings=findings_dicts,
            modules_run=modules_run,
            noise_profile=noise_profile,
        )
