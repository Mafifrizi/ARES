def _check_siem_correlations(
    techniques_executed: list[str],
    execution_timestamps: dict[str, float] | None = None,
) -> list[dict]:
    """
    Check which SIEM correlation rules would fire given executed techniques.
    Returns list of triggered rules with timing analysis.
    """
    if not techniques_executed:
        return []

    triggered = []
    tech_set = set(techniques_executed)

    for rule in _SIEM_CORRELATION_RULES:
        # Check if any trigger technique was executed
        rule_techniques = set(rule.triggers)
        matched = tech_set & rule_techniques

        if not matched:
            continue

        # Check if ALL required techniques for multi-technique rules are present
        all_required_present = all(
            any(t.startswith(req.rstrip("*")) for t in tech_set)
            for req in rule.triggers
        )

        if not all_required_present and len(rule.triggers) > 1:
            continue

        # Estimate time-to-detection
        time_to_detect = rule.within_minutes + (rule.threshold * 0.5)

        triggered.append({
            "rule":             rule.name,
            "description":      rule.description,
            "severity":         rule.severity,
            "triggered_by":     list(matched),
            "time_to_detect_min": time_to_detect,
            "common_in":        rule.common_in,
            "recommendation":   (
                f"Add {rule.within_minutes}+ minute gap between "
                f"{' and '.join(list(matched)[:2])} to avoid this rule."
            ),
        })

    return sorted(triggered, key=lambda x: x["time_to_detect_min"])



def _apply_dwell_time_decay(
    base_score:          float,
    campaign_start_time: float,
    now:                 float | None = None,
) -> float:
    """
    Longer undetected dwell = lower probability next technique triggers detection.
    Logic: if EDR hasn't caught us after N days, signatures may not match.

    Decay schedule:
      < 1 day:   0%  reduction (fresh compromise — full risk)
      1-3 days:  10% reduction
      3-7 days:  20% reduction
      1-2 weeks: 35% reduction
      2-4 weeks: 50% reduction
      1 month+:  60% reduction
    """
    now = now or time.time()
    dwell_days = max(0.0, (now - campaign_start_time) / 86400.0)

    if dwell_days < 1:
        return base_score
    elif dwell_days < 3:
        return base_score * 0.90
    elif dwell_days < 7:
        return base_score * 0.80
    elif dwell_days <= 14 + (1 / 86400):
        return base_score * 0.65
    elif dwell_days < 30:
        return base_score * 0.50
    else:
        return base_score * 0.40


@dataclass
class ActionRisk:
    module_id:    str
    technique:    str
    event_source: str
    event_id:     str
    probability:  float
    description:  str
    timestamp:    float = field(default_factory=time.monotonic)


@dataclass
class CoveragePrediction:
    overall_score:         float             # 0.0 - 1.0
    action_risks:          list[ActionRisk]
    highest_risk_action:   ActionRisk | None
    wait_hours:            int
    wait_reason:           str
    recommendations:       list[str]
    modules_analyzed:      int
    techniques_triggered:  list[str]
    soc_activity:          dict = field(default_factory=dict)  # SOC shift analysis
    siem_correlations:     list[dict] = field(default_factory=list)  # triggered SIEM rules


class CoveragePredictor:
    """
    Offline scoring engine — no network calls.
    Reads campaign findings and audit data to compute detection probability.
    """

    def predict(
        self,
        campaign_findings: list[dict],
        modules_run:       list[dict],   # list of {module_id, mitre_techniques, opsec_level, timestamp}
        noise_profile:     str = "normal",
        **kwargs: Any,
    ) -> CoveragePrediction:
        """
        Compute detection probability from campaign execution history.

        Args:
            campaign_findings: Finding dicts from campaign.findings
            modules_run:       List of modules that ran with their metadata
            noise_profile:     Campaign noise profile (stealth/normal/aggressive)

        Returns:
            CoveragePrediction with overall score and per-action breakdown
        """
        action_risks: list[ActionRisk] = []
        techniques_seen: set[str] = set()

        noise_mod = _OPSEC_MODIFIERS.get(noise_profile, 0.55)

        for mod_entry in modules_run:
            module_id  = mod_entry.get("module_id", "")
            techniques = mod_entry.get("mitre_techniques", [])
            opsec_lvl  = mod_entry.get("opsec_level", "medium").lower()
            ts         = mod_entry.get("timestamp", time.monotonic())

            opsec_mod  = _OPSEC_MODIFIERS.get(opsec_lvl, 0.55)

            for technique in techniques:
                techniques_seen.add(technique)
                events = _TECHNIQUE_DETECTION_MAP.get(technique, [])
                for evt_source, evt_id, base_prob, desc in events:
                    # Adjust probability by OPSEC level and noise profile
                    adjusted = min(base_prob * opsec_mod * noise_mod * 1.4, 1.0)
                    action_risks.append(ActionRisk(
                        module_id=module_id,
                        technique=technique,
                        event_source=evt_source,
                        event_id=evt_id,
                        probability=adjusted,
                        description=desc,
                        timestamp=ts,
                    ))

        # Compute overall score — weighted by highest individual risks
        # Apply dwell time decay — longer undetected = lower score
        campaign_start = kwargs.get("campaign_start_time")
        if campaign_start is None:
            campaign_obj = kwargs.get("campaign")
            if campaign_obj:
                raw_start = getattr(campaign_obj, "created_at", None)
                if raw_start:
                    if hasattr(raw_start, "timestamp"):
                        campaign_start = raw_start.timestamp()  # datetime → epoch
                    elif isinstance(raw_start, (int, float)):
                        campaign_start = float(raw_start)
                    else:
                        try:
                            campaign_start = float(raw_start)
                        except (TypeError, ValueError):
                            campaign_start = None  # give up gracefully
        if campaign_start and action_risks:
            for ar in action_risks:
                ar.probability = _apply_dwell_time_decay(ar.probability, campaign_start)

        if not action_risks:
            overall = 0.0
        else:
            # Use expected value formula: P(detected) = 1 - ∏(1 - p_i)
            prob_not_detected = 1.0
            for ar in action_risks:
                prob_not_detected *= (1.0 - ar.probability)
            overall = 1.0 - prob_not_detected
            overall = min(overall, 1.0)

        # Highest risk action
        highest = max(action_risks, key=lambda x: x.probability) if action_risks else None

        # Wait time recommendation
        wait_hours = 0
        wait_reason = "Continue — detection risk minimal"
        for lo, hi, hours, reason in _WAIT_RECOMMENDATIONS:
            if lo <= overall < hi:
                wait_hours = hours
                wait_reason = reason
                break

        # Generate recommendations
        recommendations = self._build_recommendations(overall, action_risks, noise_profile)

        # SOC shift analysis
        soc_activity = _compute_soc_activity_factor(
            target_domain=kwargs.get("target_domain", ""),
            target_type=kwargs.get("target_type", "default"),
        )

        # SIEM correlation analysis
        siem_hits = _check_siem_correlations(list(techniques_seen))

        # Adjust score upward if SIEM correlation rules triggered
        if siem_hits:
            critical_siem = [h for h in siem_hits if h["severity"] == "critical"]
            if critical_siem:
                overall = min(overall + 0.15, 1.0)
            elif siem_hits:
                overall = min(overall + 0.08, 1.0)

        # Adjust wait time if SOC is active
        soc_factor = soc_activity.get("soc_activity_factor", 0.5)
        if action_risks and soc_factor > 0.7 and wait_hours < 8:
            wait_hours = max(wait_hours, 8)
            wait_reason += f" (SOC currently at {soc_factor:.0%} activity)"

        # Re-run recommendations with new data
        recommendations = self._build_recommendations(
            overall, action_risks, noise_profile,
            soc_activity=soc_activity, siem_hits=siem_hits,
        )

        return CoveragePrediction(
            overall_score=overall,
            action_risks=sorted(action_risks, key=lambda x: x.probability, reverse=True),
            highest_risk_action=highest,
            wait_hours=wait_hours,
            wait_reason=wait_reason,
            recommendations=recommendations,
            modules_analyzed=len(modules_run),
            techniques_triggered=sorted(techniques_seen),
            soc_activity=soc_activity,
            siem_correlations=siem_hits,
        )

    def _build_recommendations(
        self,
        score:         float,
        risks:         list[ActionRisk],
        noise_profile: str,
        soc_activity:  dict | None = None,
        siem_hits:     list[dict] | None = None,
    ) -> list[str]:
        recs = []

        if score > 0.9:
            recs.append("CRITICAL: Assume detected. Stop all operations and reset OPSEC posture.")
            recs.append("Rotate all implants, change C2 infrastructure, use fresh credentials.")

        if score > 0.6:
            recs.append("Switch from direct lateral movement to living-off-the-land techniques.")
            recs.append("Use existing scheduled tasks or WMI instead of creating new ones.")

        if noise_profile == "aggressive" and score > 0.4:
            recs.append("Switch noise profile to NORMAL or STEALTH to reduce IOC density.")

        # Flag highest-risk techniques
        high_risk = [r for r in risks if r.probability > 0.8]
        if high_risk:
            techniques = list({r.technique for r in high_risk})
            recs.append(f"High-risk techniques detected: {', '.join(techniques[:3])}. "
                        "Consider alternative approach or accept detection risk.")

        # LSASS-specific
        if any("LSASS" in r.description for r in risks):
            recs.append("LSASS access detected — CrowdStrike/Defender ATP almost certainly alerted. "
                        "Use DPAPI or LAPS enumeration as credential alternatives.")

        # DCSync-specific
        if any("DCSync" in r.description for r in risks):
            recs.append("DCSync detected — high-confidence SIEM alert expected. "
                        "Verify krbtgt rotation has not occurred before using harvested hashes.")

        if not recs:
            recs.append("Current detection risk is acceptable. Maintain current OPSEC profile.")

        # SOC activity recommendations
        if soc_activity:
            soc_factor = soc_activity.get("soc_activity_factor", 0.0)
            if soc_factor > 0.6:
                recs.append(soc_activity.get("recommendation", ""))

        # SIEM correlation recommendations
        if siem_hits:
            fastest = siem_hits[0]
            common_rules = ", ".join(fastest["common_in"][:3])
            recs.append(
                f"SIEM ALERT: '{fastest['rule']}' would fire in ~{fastest['time_to_detect_min']} min "
                f"in {common_rules}. "
                f"{fastest['recommendation']}"
            )

        return recs


